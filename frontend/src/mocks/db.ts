import { factory, primaryKey } from '@mswjs/data'

export const db = factory({
  product: {
    id: primaryKey(String),
    name: String,
    slug: String,
    description: String,
    price: Number,
    originalPrice: Number,
    image: String,
    category: String,
    rating: Number,
    reviewCount: Number,
    stock: Number,
    isNew: Boolean,
    createdAt: String,
  },
  cartItem: {
    id: primaryKey(String),
    productId: String,
    productName: String,
    productImage: String,
    price: Number,
    quantity: Number,
  },
  coupon: {
    id: primaryKey(String),
    code: String,
    description: String,
    discountType: String,
    discountValue: Number,
    minPurchase: Number,
    maxDiscount: Number,
    expiresAt: String,
    isActive: Boolean,
    usageLimit: Number,
    usageCount: Number,
  },
  order: {
    id: primaryKey(String),
    items: Array,
    subtotal: Number,
    discount: Number,
    shipping: Number,
    total: Number,
    couponCode: String,
    paymentMethod: Object,
    status: String,
    shippingAddress: Object,
    createdAt: String,
  },
  address: {
    id: primaryKey(String),
    fullName: String,
    phone: String,
    email: String,
    address: String,
    city: String,
    state: String,
    postcode: String,
    isDefault: Boolean,
  },
})
